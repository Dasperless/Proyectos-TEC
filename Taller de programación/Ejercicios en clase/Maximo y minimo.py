def maximo (x,y,z):
    if x > y and z:
        return print ("el valor máximo es", x)
    elif y > z:
        return print ("el valor máximo es", y)
    else:
        return print ("el valor máximo es", z)

def minimo (x,y,z):
    if x < y and z:
        return print ("el valor mínimo es", x)
    elif y < z:
        return print ("el valor mínimo es", y)
    else:
        return print ("el valor minimo es", z)
