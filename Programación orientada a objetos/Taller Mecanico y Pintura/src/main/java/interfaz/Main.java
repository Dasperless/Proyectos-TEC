    private void llenarListasFinalizarServicio() {
        DefaultTableModel modelMG = (DefaultTableModel) listaServiciosMG.getModel();
        DefaultTableModel modelEP = (DefaultTableModel) listaServiciosEP.getModel();
        modelMG.setRowCount(0);
        modelEP.setRowCount(0);
        DateFormat dateFormat = new SimpleDateFormat("d-MM-y");
        for (int i = 0; i < listaServicios.size(); i++) {
            Object actual = listaServicios.get(i);
            if (actual instanceof MecanicaGeneral) {
                MecanicaGeneral servicioMG = (MecanicaGeneral) actual;
                String strFechaEntrega = dateFormat.format(servicioMG.getFechaEntrega());
                String strFechaRecibido = dateFormat.format(servicioMG.getFechaRecibido());
                ArrayList<Empleado> listaEmpleados = servicioMG.getEmpleados();
                String strEmpleados = "";
                for (Empleado listaEmpleado : listaEmpleados) {
                    strEmpleados += listaEmpleado.getNombreEmpleado() + "\n";

                }
                modelMG.addRow(new Object[]{i, servicioMG.getEstado(), servicioMG.getCosto(), servicioMG.getCedulaCliente(), servicioMG.getDescripcionVehiculo(),
                    servicioMG.getDescripcionProblema(), servicioMG.getPlacaVehiculo(), strFechaEntrega, strFechaRecibido, strEmpleados});
            } else {
                EnderezadoyPintura servicioEP = (EnderezadoyPintura) actual;
                String strPoliza = "";
                if (servicioEP.getPoliza()) {
                    strPoliza = "Sí";
                } else {
                    strPoliza = "No";
                }
                String strFechaEntrega = dateFormat.format(servicioEP.getFechaEntrega());
                String strFechaRecibido = dateFormat.format(servicioEP.getFechaRecibido());
                ArrayList<Empleado> listaEmpleados = servicioEP.getEmpleados();
                String strEmpleados = "";
                for (Empleado listaEmpleado : listaEmpleados) {
                    strEmpleados += listaEmpleado.getNombreEmpleado() + "\n";
                }
                modelEP.addRow(new Object[]{i, servicioEP.getNumeroCaso(), servicioEP.getCosto(), servicioEP.getCedulaCliente(), servicioEP.getPlacaVehiculo(), servicioEP.getDescripcionVehiculo(),
                    servicioEP.getPartesEnderezarPintar(), strPoliza, strFechaEntrega, strFechaRecibido, strEmpleados});
            }
        }
    }

