    private void mostrarBuscado(String buscar, int filtro, String visualizarPor) {
        DefaultTableModel model = (DefaultTableModel) tablaConsultaServicio.getModel();
        model.setRowCount(0);

        if (filtro == 0) {
            for (int i = 0; i < listaServicios.size(); i++) {
                if (listaServicios.get(i) instanceof MecanicaGeneral) {
                    MecanicaGeneral servicioMG = (MecanicaGeneral) listaServicios.get(i);
                    if (servicioMG.getPlacaVehiculo().equals(buscar) && (servicioMG.getEstado().equals(visualizarPor) || visualizarPor.equals("Todos"))) {
                        model.addRow(new Object[]{i, servicioMG.getEstado(), servicioMG.getPlacaVehiculo(), servicioMG.getCedulaCliente()});
                    }
                } else {
                    EnderezadoyPintura servicioEP = (EnderezadoyPintura) listaServicios.get(i);
                    if (servicioEP.getPlacaVehiculo().equals(buscar) && (servicioEP.getEstado().equals(visualizarPor) || visualizarPor.equals("Todos"))) {
                        model.addRow(new Object[]{i, servicioEP.getEstado(), servicioEP.getPlacaVehiculo(), servicioEP.getCedulaCliente()});
                    }
                }
            }
        } else {
            int cedula = Integer.valueOf(buscar);
            for (int i = 0; i < listaServicios.size(); i++) {
                if (listaServicios.get(i) instanceof MecanicaGeneral) {
                    MecanicaGeneral servicioMG = (MecanicaGeneral) listaServicios.get(i);
                    if (servicioMG.getCedulaCliente() == cedula && (servicioMG.getEstado().equals(visualizarPor) || visualizarPor.equals("Todos"))) {
                        model.addRow(new Object[]{i, servicioMG.getEstado(), servicioMG.getPlacaVehiculo(), servicioMG.getCedulaCliente()});
                    }
                } else {
                    EnderezadoyPintura servicioEP = (EnderezadoyPintura) listaServicios.get(i);
                    if (servicioEP.getCedulaCliente() == cedula && (servicioEP.getEstado().equals(visualizarPor) || visualizarPor.equals("Todos"))) {
                        model.addRow(new Object[]{i, servicioEP.getEstado(), servicioEP.getPlacaVehiculo(), servicioEP.getCedulaCliente()});
                    }
                }
            }
        }
    }

    private boolean isMGTableSelected() {
        for (int i = 0; i < listaServiciosMG.getRowCount(); i++) {
            if (listaServiciosMG.isRowSelected(i)) {
                return true;
            }
        }
        return false;
    }

