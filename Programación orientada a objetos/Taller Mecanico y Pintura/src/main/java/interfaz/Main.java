    private void eliminarMarcasMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_eliminarMarcasMouseClicked
        int[] itemsSelec = jList2.getSelectedIndices();
        int eliminados = 0;
        for (int i = 0; i < itemsSelec.length; i++) {
            for (int j = 0; j < vehiculos.size(); j++) {
                Vehiculo vehiculoActual = vehiculos.get(j);
                ArrayList<String> categorias = marcas.getCategorias();
                if (vehiculoActual.getMarca().equals(categorias.get(itemsSelec[i] - eliminados))) {
                    JOptionPane.showMessageDialog(null, "La marca \n" + modelos.get(itemsSelec[i]).getDescripModelo() + "\n está asociado a un vehiculo");
                } else {
                    marcas.eliminarCategoria(itemsSelec[i] - eliminados);
                    eliminados++;
                }
            }
        }
        actualizarListaEliminados();
    }//GEN-LAST:event_eliminarMarcasMouseClicked

