    /**
     * Este metodo verifica el input obtenido del tablero.
     *
     * @param evt evento
     */
    private void boardInput(java.awt.event.KeyEvent evt) {
        JTextField field = (JTextField) evt.getSource();    //Objeto generador del evento.
        int valueLength = field.getText().length();         //Tamao del input.
        char c = evt.getKeyChar();                          //Caracter actual
        int charVal = ((int) c) - 48;

        //Si el tamao del input no es un dgito o el input no es una unidad.
        if (!Character.isDigit(c) || valueLength > 0 || charVal == 0) {
            evt.consume();
        }
        String name = field.getName();
        int i = Integer.parseInt(name.substring(1, 2));
        int j = Integer.parseInt(name.substring(3, 4));
        if (charVal == -40) {
            currentBoard[i][j] = 0;
        } else if (charVal >= 1 && charVal <= 9) {
            currentBoard[i][j] = charVal;
            numInput++;
        }

    }

    /**
     * Establece la cantidad de sugerencias.
     *
     * @param num La cantidad de sugerencias.
     */
    private void setSuggestions(int num) {
        numSuggestions = num;
        Suggestions.setText(String.format("Sugerencias: %d", numSuggestions));
    }

    /**
     * Inicia un nuevo juego
     */
    private void newGame() {
        controller.newBoard();
        copyBoard(controller.getBoardClues());
        for (var fields : componentMap.values()) {
            JTextField field = (JTextField) fields;
            if (!field.isEnabled()) {
                field.setEnabled(true);
            }
            field.setText("");
            field.setForeground(Color.black);
        }
        if (completionState.equals("")) {
            completionState = "Abandono";
        }
        showStatistics();
        setBoard();
    }

    private void showStatistics() {
        String str;
        str = String.format("Cantidad de celdas de ingreso de dgitos:  %d\n"
                + "Cantidad de verificaciones realizadas: %d\n"
                + "Cantidad de errores de verificacin: %d\n"
                + "Cantidad de sugerencias utilizadas: %d\n"
                + "Tipo Finalizacin: %s", numInput, numVerif, numVerifErr, numSuggestions, completionState);
        JOptionPane.showMessageDialog(null, str);
        numInput = 0;
        numVerif = 0;
        numVerifErr = 0;
        numSuggestions = 0;
        completionState = "";
    }

    /**
     * Muestra las soluciones en el tablero
     */
    private void seeSolution() {
        int[][] board = controller.getBoard();
        for (int i = 0; i < 9; i++) {
            for (int j = 0; j < 9; j++) {
                String name = String.format("P%d_%d", i, j);
                JTextField field = (JTextField) getComponentByName(name);
                String value = String.valueOf(board[i][j]);
                field.setText(value);
                currentBoard[i][j] = Integer.parseInt(value);
            }
        }
        completionState = "Autosolucion";
    }

    /**
     * Reinicia el tablero
     */
    private void restart() {
        copyBoard(controller.getBoardClues());
        for (int i = 0; i < 9; i++) {
            for (int j = 0; j < 9; j++) {
                String name = String.format("P%d_%d", i, j);
                JTextField field = (JTextField) getComponentByName(name);
                if (field.isEnabled()) {
                    if (currentBoard[i][j] == 0) {
                        field.setText("");
                    } else {
                        field.setText(String.valueOf(currentBoard[i][j]));
                    }
                    if (!field.isEditable()) {
                        field.setEditable(true);
                    }
                    field.setForeground(Color.black);
                }
            }
        }
        setSuggestions(5);
    }

    /**
     * Copia la matriz de pistas al tablero de la interfaz.
     *
     * @param pBoard la matriz de pistas.
     */
    private void copyBoard(int[][] pBoard) {
        for (int i = 0; i < 9; i++) {
            System.arraycopy(pBoard[i], 0, currentBoard[i], 0, 9);
        }
    }

    /**
     * Obtiene los textfields del tablero y los convierte en un hashmap
     */
    private void createComponentMap() {
        componentMap = new HashMap<String, Component>();
        Component[] components = Tablero.getComponents();
        for (Component component : components) {
            JPanel square = (JPanel) component;
            Component[] fields = square.getComponents();
            for (Component field : fields) {
                componentMap.put(field.getName(), field);
            }
        }

    }

    /**
     * Establece las pistas del tablero
     */
    private void setBoard() {
        for (int i = 0; i < 9; i++) {
            for (int j = 0; j < 9; j++) {
                String name = String.format("P%d_%d", i, j);
                JTextField field = (JTextField) getComponentByName(name);
                if (currentBoard[i][j] != 0) {
                    field.setText(String.valueOf(currentBoard[i][j]));
                    field.setEnabled(false);
                }
            }
        }
    }

    /**
     * Retorna un ndice aleatorio
     *
     * @return un ndice aleatorio
     */
    private int randomIndex() {
        Random r = new Random();
        int i = r.nextInt(9);
        return i;
    }

    /**
     * Muestra una nueva sugerencia en el tablero.
     */
    private void newSuggestion() {
        boolean isEmpty = false;
        while (!isEmpty) {
            int i = randomIndex();
            int j = randomIndex();
            String name = String.format("P%d_%d", i, j);
            JTextField field = (JTextField) getComponentByName(name);
            if (field.isEditable() && currentBoard[i][j] == 0) {
                isEmpty = true;
                currentBoard[i][j] = controller.getBoard()[i][j];
                field.setForeground(Color.green);
                field.setText(String.valueOf(currentBoard[i][j]));
                field.setEditable(false);
            }
        }
        numSuggestions++;
    }

    /**
     * Obtiene los componentes por nombre
     *
     * @param name el nombre del componente
     * @return el componente
     */
    public Component getComponentByName(String name) {
        if (componentMap.containsKey(name)) {
            return (Component) componentMap.get(name);
        } else {
            return null;
        }
    }
    
    /**
     * Verifica si las soluciones del tablero son correctas.
     */
    private void verify() {
        int[][] board = controller.getBoard();
        System.out.println(Arrays.deepToString(currentBoard));
        System.out.println(Arrays.deepToString(board));
        int empty = 0;
        int incorrect = 0;
        for (int i = 0; i < 9; i++) {
            for (int j = 0; j < 9; j++) {
                String name = String.format("P%d_%d", i, j);
                JTextField field = (JTextField) getComponentByName(name);
                if (currentBoard[i][j] == 0) {
                    empty++;
                } else if (currentBoard[i][j] != board[i][j]) {
                    incorrect++;
                    numVerifErr++;
                }
            }
        }

        if (empty == 0 && incorrect == 0) {
            JOptionPane.showMessageDialog(null, "juego finalizado exitosamente, felicidades!!!");
            if (completionState.equals("")) {
                completionState = "Exitosa";

            }
        } else {
            String str = String.format("hay %d dgitos incorrecto(s) y hay %d celda(s) vacas de %d", incorrect, empty, 64);
            JOptionPane.showMessageDialog(null, str);
        }
        numVerif++;
    }

